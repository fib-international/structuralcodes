    @classmethod
    def get_polygon(cls, name: str) -> Polygon:
        """Returns a shapely polygon representing a W section."""
        parameters = cls.parameters.get(name)
        if parameters is None:
            raise ValueError(
                f"Profile '{name}' not found in W sections. "
                "Select a valid profile (available ones: "
                f"{cls.profiles()})"
            )
        return _create_I_section(**parameters)

    @classmethod
    def profiles(cls) -> list:
        """Returns a list containing all available profiles."""
        return list(cls.parameters.keys())

    def __init__(self, name: str) -> None:
        """Creates a new IPE object."""
        if isinstance(name, (float, int)):
            name = f'IPE{int(name):0d}'
        parameters = self.parameters.get(name)
        if parameters is None:
            raise ValueError(
                f"Profile '{name}' not found in W sections. "
                "Select a valid profile (available ones: "
                f"{self.profiles()})"
            )
        super().__init__()
        self._h = parameters.get('h')
        self._b = parameters.get('b')
        self._tw = parameters.get('tw')
        self._tf = parameters.get('tf')
        self._r = parameters.get('r')
        self._polygon = _create_I_section(**parameters)

    @property
    def polygon(self) -> Polygon:
        """Returns shapely Polygon of section.

        Returns:
            Polygon: The represention of the W section.
        """
        return self._polygon

    @property
    def h(self) -> float:
        """Returns height of W section.

        Returns:
            float: Height h of W section.
        """
        return self._h

    @property
    def b(self) -> float:
        """Returns width of W section.

        Returns:
            float: Width b of W section.
        """
        return self._b

    @property
    def tw(self) -> float:
        """Returns thickness of web of W section.

        Returns:
            float: Web thickness tw of W section.
        """
        return self._tw

    @property
    def tf(self) -> float:
        """Returns thickness of flange of W section.

        Returns:
            float: Flange thickness tf of W section.
        """
        return self._tf

    @property
    def r(self) -> float:
        """Returns fillet radius of W section.

        Returns:
            float: Fillet radius r of W section.
        """
        return self._r
